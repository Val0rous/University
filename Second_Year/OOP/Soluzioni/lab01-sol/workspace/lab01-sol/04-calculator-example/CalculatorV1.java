class CalculatorV1 {

    void build() {
    }

    double add(double n1, double n2) {
        return n1 + n2;
    }

    double sub(double n1, double n2) {
        return n1 - n2;
    }

    double mul(double n1, double n2) {
        return n1 * n2;
    }

    double div(double n1, double n2) {
        return n1 / n2;
    }
}
